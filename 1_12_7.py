x = int(input())
x1 = x // 100000
x2 = (x - x1 * 100000) // 10000
x3 = (x - x1 * 100000 - x2 * 10000) // 1000
x4 = (x - x1 * 100000 - x2 * 10000 - x3 * 1000) // 100
x5 = (x - x1 * 100000 - x2 * 10000 - x3 * 1000 - x4 * 100) // 10
x6 = (x - x1 * 100000 - x2 * 10000 - x3 * 1000 - x4 * 100 - x5 * 10)
if x1 + x2 + x3 == x4 + x5 + x6:
    print('Счастливый')
else:
    print('Обычный')